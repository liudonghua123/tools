# Python Basic: Control Flow

# If-Else
age = 18
if age < 13:
    print("Child")
elif age < 20:
    print("Teenager")
else:
    print("Adult")

# For loop
print("Iterating over a list:")
for fruit in ["apple", "banana"]:
    print(fruit)

# While loop
print("Counting down:")
count = 3
while count > 0:
    print(count)
    count -= 1
