# Python Basic: Data Types
a = 10          # int
b = 3.14        # float
c = "Hello"     # str
d = True        # bool

print(f"a: {a}, type: {type(a)}")
print(f"b: {b}, type: {type(b)}")
print(f"c: {c}, type: {type(c)}")
print(f"d: {d}, type: {type(d)}")

# Basic operations
print("a + b =", a + b)
print("c * 2 =", c * 2)
